const FormInput = ({ label, children, ...otherProps }) => {
  return (
    <div className="group">
      <input {...otherProps} />
      {label && (
        <label
          className={`${
            otherProps.value.length ? "shrink" : ""
          } form-input-label`}
        >
          {label}
        </label>
      )}
    </div>
  );
};

export default FormInput;
